package P98_Practice.p31_p60.p31;

public class Member {
    private String name;
    private String sex;
    private int age;

    public Member(String name, String sex, int age) {
        this.name = name;
        this.sex = sex;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public int getAge() {
        return age;
    }
}
